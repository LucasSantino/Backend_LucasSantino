//EXPLO 2 POP(pop) e PUSH(push)

notas =[5,10,7.5];
console.log(notas);
notas.pop(); /// pop REMOVE um elemento do array
console.log(notas);
notas.pop(); /// pop REMOVE um elemento do array
console.log(notas);
notas.push(9) /// push ADICIONA um elemento no array
console.log(notas);
notas.push(10) /// push ADICIONA um elemento no array
console.log(notas)
